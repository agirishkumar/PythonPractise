# demonstrate String slicing

String1 = "GeeksForGeeks"
print("Initial String: ",end=" ")
print(String1)

#printing 3rd to 12th character
print("Slicing characters from 3 to 12: ",end=" ")
print(String1[3:12])

#printing 3rd to 2nd last character
print("Slicing characters from 3 to 2nd last: ",end=" ")
print(String1[3:-2])